class Calculadora:
    @staticmethod
    def somar(num1, num2):
        return num1 + num2

    @staticmethod
    def subtrair(num1, num2):
        return num1 - num2

    @staticmethod
    def multiplicar(num1, num2):
        return num1 * num2

    @staticmethod
    def dividir(num1, num2):
        return num1 / num2